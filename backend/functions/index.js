const {onRequest} = require("firebase-functions/v2/https");
const logger = require("firebase-functions/logger");
const {GoogleGenerativeAI} = require("@google/generative-ai");

// ラジオ番組を生成するために呼び出すエンドポイント
exports.generateRadio = onRequest(
    {region: "asia-northeast1", maxInstances: 1, cors: ["*"]},
    async (request, response) => {
      try {
        if (request.method !== "POST") {
          response.status(405).send("Method Not Allowed");
          return;
        }
        /**
       * @type {{ category:string[] }}
       */
        const {category} = request.body;
        if (!category || !Array.isArray(category) || category.length === 0) {
          response.status(400).json({
            error: "Bad Request",
            reason: "category are required.",
          });
          return;
        }
        const result = await runRadioAI(category);
        console.log(process.env.GEMINI_API_KEY);
        logger.log(result);
        logger.log(request.body);
        response.status(200).send(result);
      } catch (error) {
        logger.error(error);
        response.status(500).send("Internal Server Error");
      }
    },
);

// ラジオリスナーからの相談に乗るセリフを呼び出すエンドポイント
exports.generateAnswer = onRequest(
    {region: "asia-northeast1", maxInstances: 1, cors: ["*"]},
    async (request, response) => {
      try {
        if (request.method !== "POST") {
          response.status(405).send("Method Not Allowed");
          return;
        }
        /**
       * @type {{ radioname:string, text:string }}
       */
        const {radioname, text} = request.body;
        if (!radioname || !text) {
          response.status(400).json({
            error: "Bad Request",
            reason: "radioname or text are required.",
          });
          return;
        }
        const result = await runAnswerAI(radioname, text);
        console.log(process.env.GEMINI_API_KEY);
        logger.log(result);
        logger.log(request.body);
        response.status(200).send(result);
      } catch (error) {
        logger.error(error);
        response.status(500).send("Internal Server Error");
      }
    },
);

